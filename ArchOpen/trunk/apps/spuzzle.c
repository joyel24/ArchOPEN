coord GetXY(int pos)
{
    coord xy;
    int index = 0;

    x = 0;
    y = 0;

    do
    {
        if(x+1 < width) x++; else x=0;
        if(x==0) y++;

        index++;
    }
    while ( index < pos );

    xy.x = x;
    xy.y = y;

    return xy;
}

void GenerateField()
{
    int i = 0;
    int j = 0;
    int map_size = height*width;
    int pos1 = 0;
    int pos2 = 0;
    int index = 0;
    int tmpIdent = 0;
    coord xy1;
    coord xy2;
    coord tmp;

    // fill map
    while ( index < map_size)
    {
        field[j][i] = index;

        if(i+1 < width) i++; else i=0;
        if(i==0) j++;

        index++;
    }

    // unsort these pairs
    for ( i = 0; i < map_size * 20; i++ )
    {
        pos1 = rand() % map_size;
        pos2 = rand() % map_size;

        // swap
        xy1 = GetXY(pos1);
        xy2 = GetXY(pos2);

        tmpIdent = field[xy1.y][xy1.x];
        field[xy1.y][xy1.x] = field[xy2.y][xy2.x];
        field[xy2.y][xy2.x] = tmpIdent;
    }

    tmp = SearchBlankField();
    x = tmp.x;
    y = tmp.y;
}

coord SearchBlankField()
{
    int i=0,j=0;
    coord tmp;
    memset(&tmp, -1, sizeof(tmp));

	//display the field
	for(i=0;i<height;i++)
	{
        for(j=0;j<width;j++)
        {
            if(field[i][j] == 0)
            {
                tmp.x = j;
                tmp.y = i;
                return tmp;
            }
        }
    }

    return tmp;
}

int checkOrder()
{
    int i = 0;
    int j = 0;
    int index = 1;
    int map_size = height*width;

    while ( index < map_size-1)
    {
        if(field[j][i] != index)
        {
            return 0; // wrong order
        }

        if(i+1 < width) i++; else i=0;
        if(i==0) j++;

        index++;
    }

    return 1;
}

