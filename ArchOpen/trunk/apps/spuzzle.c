void eventHandlerLoop(void)
{
    int oldx;
    int oldy;
	int stop=0;
	int evt,evt_handler;
	
	evt_handler=evt_getHandler(BTN_CLASS);

	while(!stop)
	{
        evt=evt_getStatusBlocking(evt_handler);
		if(evt==NO_EVENT)
			continue;
        printf("evt=%s\n",getBtnName(evt));
		oldx = x;
		oldy = y;
		
		switch (evt)
		{
			case BTN_OFF:
				stop=1;
				break;

					/* move cursor right */
			case BTN_RIGHT:
				if(x-1 >= 0)
				{
					x--;
					turns++;
					showTurns();
					field[oldy][oldx] = field[y][x];
					field[y][x] = 0;
					displayField();
					if(checkOrder())
					   endOfGame();
				}
				break;

				/* move cursor left */
				case BTN_LEFT:
					if(x+1 < width)
					{
						x++;
						turns++;
						showTurns();
						field[oldy][oldx] = field[y][x];
						field[y][x] = 0;
						displayField();
						if(checkOrder())
						   endOfGame();
					}
					break;

					/* move cursor up */
			case BTN_UP:
				if(y+1 < height)
				{
					y++;
					turns++;
					showTurns();
					field[oldy][oldx] = field[y][x];
					field[y][x] = 0;
					displayField();
					if(checkOrder())
					   endOfGame();
				}
				break;

					/* move cursor down */
			case BTN_DOWN:
				if(y-1 >= 0)
				{
					y--;
					turns++;
					showTurns();
					field[oldy][oldx] = field[y][x];
					field[y][x] = 0;
					displayField();
					if(checkOrder())
					   endOfGame();
				}
				break;

// new game
#ifdef AV4XX
                case BTN_F1:
#endif
#ifndef AV4XX
                case BTN_ON:
#endif
				gfx_clearScreen(COLOR_GREEN);
				init();
				displayField();
				break;
		}
    }
	evt_freeHandler(evt_handler);
}

