void eventHandlerLoop(void)
{
    pair tmp;
    int gameover = 0;
    testpair test;
	int evt;
	int evt_handler=evt_getHandler(BTN_CLASS);
	int stop=0;
	while(!stop)
	{
		evt=evt_getStatus(evt_handler);
		if(evt==NO_EVENT)
			continue;
		if(GameMode == 0)
		{
			switch (evt) {
				case BTN_OFF:
						/* get out of here */
						stop=1;
						break;
	
						/* move cursor left */
					case BTN_LEFT:
						setCursor(1);
						if(x-1 >= 0)
						   x--;
						else
						   x = width-1;
	
						setCursor(0);
						break;
	
						/* move cursor right */
					case BTN_RIGHT:
						setCursor(1);
						if(x+1 >= width)
						   x = 0;
						else
						   x++;
						setCursor(0);
						break;
	
						/* move cursor down */
					case BTN_DOWN:
						setCursor(1);
						if(y+1 >= height)
						   y = 0;
						else
						   y++;
						setCursor(0);
						break;
	
						/* move cursor up */
					case BTN_UP:
						setCursor(1);
						if(y-1 >= 0)
						   y--;
						else
						   y = height-1;
						setCursor(0);
						break;
	
					case BTN_F2:
	//					displayField(1);
	//                    endOfGame();
						break;
	
						/* toggle flag under cursor */
#ifdef AV4XX
                case BTN_ON:
#endif
#ifndef AV4XX
                case BTN_F1:
#endif	
						test = testField(); // gibt es schon ein anderes auf test gesetzes Feld ?
	
						if( ((test.x != x) || (test.y != y)) && (field[y][x].known == 0) ) // wurde 2 mal das gleiche Feld angeklickt ? Das aktuelle Feld darf auch noch nicht aufgedeckt sein !
						{
							field[y][x].test = 1;
							displayField(0);
	
							// Es war das 2. auf test gesetzte Teil --> prfen ob es gleich Teile sind
							if(test.test > 0)
							{
								turns++;
								showTurns();
	
								tmp = getPair();
	
								if(testPair() == 1) // Paar gefunden ?
								{
									field[tmp.y1][tmp.x1].known = 1;
									field[tmp.y2][tmp.x2].known = 1;
	
									cntPairsLeft--;
	
									showCntPairs();
	
									if(cntPairsLeft == 0)
									{
										endOfGame();
										gameover = 1;
									}
								}
								else
								{
									mdelay(100);
								}
	
								field[tmp.y1][tmp.x1].test = 0;
								field[tmp.y2][tmp.x2].test = 0;
	
								if(gameover == 0)
								{
									displayField(0);
								}
								else
								{
									gameover = 0;
								}
							}
						}
						break;
// new game
#ifdef AV4XX
                case BTN_F1:
#endif
#ifndef AV4XX
                case BTN_ON:
#endif
						gfx_clearScreen(COLOR_GREEN);
						init();
						displayField(0);
						setCursor(0);
						break;
	
					case BTN_F3: // settings
	
						gfx_clearScreen(COLOR_GREEN);
						gfx_putS(COLOR_BLACK, COLOR_GREEN, 287,47, "Leave");
						GameMode = 1;
						init_settings_screen();
						break;
			}
		}
		else
		{
			switch (evt) {
			   case BTN_UP:
					if(matrixSize == 0)
					   matrixSize = 2;
					else
					   matrixSize--;
	
					RefreshSettings();
					break;
	
			   case BTN_DOWN:
					if(matrixSize == 2)
					   matrixSize = 0;
					else
					   matrixSize++;
	
					RefreshSettings();
				   break;
	
				case BTN_OFF:
					writeINI();
					gfx_clearScreen(COLOR_GREEN);
					GameMode = 0;
					init();
					break;
			}
	
		}
	}	
	evt_freeHandler(evt_handler);
}

testpair testField()
{
    int i=0,j=0;
    testpair tmp;

    memset(&tmp, -1, sizeof(tmp));

	for(i=0;i<height;i++)
	{
        for(j=0;j<width;j++)
        {
            if(field[i][j].test)
            {
                tmp.x = j;
                tmp.y = i;
                tmp.test = field[i][j].identifier;
                return tmp;
            }
        }
	}

    return tmp;
}

pair getPair()
{
    pair tmp;
    int i=0,j=0;
    int count = 0;

	for(i=0;i<height;i++)
	{
        for(j=0;j<width;j++)
        {
            if(field[i][j].test)
            {
                if(count==0)
                {
                    tmp.x1 = j;
                    tmp.y1 = i;
                }
                else
                {
                    tmp.x2 = j;
                    tmp.y2 = i;
                }

                count++;
            }
        }
	}

    return tmp;
}

int testPair()
{
    int i=0,j=0;
    int ident1 = 0;
    int ident2 = 0;
    int count = 0;

	for(i=0;i<height;i++)
	{
        for(j=0;j<width;j++)
        {
            if(field[i][j].test)
            {
                if(count==0)
                    ident1 = field[i][j].identifier;
                else
                    ident2 = field[i][j].identifier;

                count++;
            }
        }
	}

    // Paar gefunden ?
    if(ident1 == ident2)
    {
        return 1;
    }

    return 0;
}

